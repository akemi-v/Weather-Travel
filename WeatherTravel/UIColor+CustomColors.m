#import "UIColor+CustomColors.h"

@implementation UIColor (CustomColors)

+ (UIColor *)customBlue
{
    return [UIColor colorWithRed:0.50 green:0.81 blue:0.84 alpha:1.0];
}

+ (UIColor *)customYellow
{
    return [UIColor colorWithRed:1.00 green:0.98 blue:0.85 alpha:1.0];
}

+ (UIColor *)customPaleBlue
{
    return [UIColor colorWithRed:0.84 green:0.96 blue:0.90 alpha:1.0];
}

+ (UIColor *)customGloomyBlue
{
    return [UIColor colorWithRed:0.38 green:0.53 blue:0.52 alpha:1.0];
}

@end
